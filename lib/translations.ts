export type TranslationKey = keyof typeof translations.en;

export const useTranslation = (lang: Language) => {
  const t = (key: TranslationKey, params?: Record<string, string | number>) => {
    const set = translations[lang] || translations.en;
    const rawValue = (set as any)[key] || (translations.en as any)[key] || key;

    if (!params || typeof rawValue !== 'string') {
      return rawValue;
    }

    return Object.entries(params).reduce((message, [paramKey, paramValue]) => {
      return message.replaceAll(`{${paramKey}}`, String(paramValue));
    }, rawValue);
  };
  return { t, isRTL: lang === 'ar' };
};
